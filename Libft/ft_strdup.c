#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *a;

	a = (char*)malloc(sizeof(*s1) * (ft_strlen(s1) + 1));
	if (a)
		ft_strcpy(a, s1);
	return (a);
}
