#include <stdio.h>
#include <string.h>

int	ft_atoi(const char *str)
{
	int a;
	int b;

	a = 0;
	b = 1;
	while ((*str >= '0' && *str <= '9') || *str == '\t' || *str == '\n' ||
			*str == '\v' || *str == '\f' || *str == '\r' || *str == ' ' ||
					*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			b = b * -1;
		}
		else if (*str >= '0' && *str <= '9')
		{
			a = a * 10 + (*str - '0');
		}
		str++;
	}
	return (a * b);
}
